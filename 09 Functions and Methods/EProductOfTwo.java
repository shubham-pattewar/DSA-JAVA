public class EProductOfTwo {
    public static int product(int a, int b){
        int product = a * b;
        return product;
    }
    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        int ans = product(a, b);
        System.out.println("Ans: " + ans);
        System.out.println(ans);   
    }
}
