public class EAssignmentOperators {
    public static void main(String[] args) {
        int A = 20;
        // A = A + 10;
        A += 10;
        System.out.println(A);

        int B = 10;
        B -= 5;
        System.out.println(B);

        int C = 5;
        C /= 2;
        System.out.println(C);
    }
}
