public class CRelationalOperators {
    public static void main(String[] args) {
        int A = 10;
        int B = 5;

        System.out.println(A == B);
        System.out.println(10 == A);

        System.out.println(A != B);
        System.out.println(B != 5);

        System.out.println(A > B);
        System.out.println(B > A);

        System.out.println(A < B);
        System.out.println(B < A);

        System.out.println(10 >= A);
        System.out.println(B >= A);

        System.out.println(B <= 5);
        System.out.println(B <= A);
    }
}
